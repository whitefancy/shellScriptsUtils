13. free命令
free [选项] [-s (间隔秒数)]
选项	说明
-b	以字节为单位显示内存使用情况
-m	以MB为单位显示内存使用情况
-K	以KB为单位显示内存使用情况
-t	显示内存总和列
-s (间隔秒数)	每隔_秒持续显示
-o	不现实系统缓冲区队列

14. ps命令
显示系统进程瞬间运行状态
ps [选项]
选项	说明
a	显示所有用户进程，包含每个程序完整路径
-x	显示所有系统程序
-u	显示使用者名称与起始时间
-f	详细显示程序的路径群
-c	只显示进程的名字不显示完整路径
-e	除内核进程以外所有进程信息写到标准输出

