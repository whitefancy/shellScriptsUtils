二、系统管理与维护
1. ls命令
dir命令与ls命令相同，显示工作目录下的内容，列出所含文件与子目录
ls [选项] [路径或文件]

选项	说明
-a	显示所有文件与子目录，包括隐藏文件
-d	只显示目录列表
-l	同时列出权限、使用者、大小等
-s	文件名后输出文件的大小
-k	k字节形式表示文件大小
-u	以访问时间排序
-t	以时间排序
-o	显示除组外详细信息
-x	按列输出，横向排序
-r	反向排序
-q	用?代替不可输出字符
-m	横向输出文件名，以 , 作为分隔符
-S	以文件大小排序
-R	列出所有子目录下文件
-pF	文件名后说明类型，*是可执行普通文件，/是目录，@是符号链接，|是FIFO，=是套接字
-C	按列输出，纵向排序
-Q	把输出文件名用双引号括起来

2. pwd命令
显示当前工作目录，绝对路径
3. cd命令
cd [目录名]
选项	说明
cd 目录	切换到该目录，区分大小写
cd 或 cd ~	返回当前用户默认工作目录
cd ~用户名	返回指定用户工作目录
cd ..	返回到上级目录
cd /	返回到根目录
